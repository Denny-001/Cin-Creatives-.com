// ====================================================
// CINEMATIC 3D ANIMATIONS FOR ABOUT PAGE
// ====================================================

document.addEventListener('DOMContentLoaded', function() {
    
    // ====================================================
    // SECTION 1: FLOATING 3D CUBES IN HERO
    // ====================================================
    function initFloatingCubes() {
        const container = document.getElementById('cubes-container');
        if (!container) return;
        
        const scene = new THREE.Scene();
        const camera = new THREE.PerspectiveCamera(75, window.innerWidth / window.innerHeight, 0.1, 1000);
        const renderer = new THREE.WebGLRenderer({ alpha: true, antialias: true });
        
        renderer.setSize(window.innerWidth, window.innerHeight);
        renderer.setPixelRatio(window.devicePixelRatio);
        container.appendChild(renderer.domElement);
        
        camera.position.z = 15;
        
        // Add ambient light
        const ambientLight = new THREE.AmbientLight(0x404040);
        scene.add(ambientLight);
        
        // Add point lights for dramatic effect
        const light1 = new THREE.PointLight(0x5DD62C, 1, 30);
        light1.position.set(5, 5, 5);
        scene.add(light1);
        
        const light2 = new THREE.PointLight(0x337418, 1, 30);
        light2.position.set(-5, -3, 5);
        scene.add(light2);
        
        const light3 = new THREE.PointLight(0xffffff, 0.5, 30);
        light3.position.set(0, 5, 10);
        scene.add(light3);
        
        // Create floating cubes with different colors and sizes
        const cubes = [];
        const geometries = [
            new THREE.BoxGeometry(1, 1, 1),
            new THREE.BoxGeometry(0.8, 0.8, 0.8),
            new THREE.BoxGeometry(1.2, 1.2, 1.2),
            new THREE.BoxGeometry(0.6, 0.6, 0.6),
            new THREE.BoxGeometry(0.9, 0.9, 0.9),
            new THREE.BoxGeometry(1.1, 1.1, 1.1)
        ];
        
        const colors = [0x5DD62C, 0x337418, 0x8BC34A, 0x4CAF50, 0x2E7D32, 0x1B5E20];
        
        for (let i = 0; i < 20; i++) {
            const geometry = geometries[Math.floor(Math.random() * geometries.length)];
            const material = new THREE.MeshStandardMaterial({
                color: colors[Math.floor(Math.random() * colors.length)],
                emissive: 0x112211,
                roughness: 0.3,
                metalness: 0.1,
                transparent: true,
                opacity: 0.7 + Math.random() * 0.3
            });
            
            const cube = new THREE.Mesh(geometry, material);
            
            // Random positions in a sphere
            const radius = 5 + Math.random() * 5;
            const theta = Math.random() * Math.PI * 2;
            const phi = Math.acos(2 * Math.random() - 1);
            
            cube.position.x = radius * Math.sin(phi) * Math.cos(theta);
            cube.position.y = radius * Math.sin(phi) * Math.sin(theta);
            cube.position.z = radius * Math.cos(phi);
            
            // Store animation properties
            cube.userData = {
                speed: 0.001 + Math.random() * 0.003,
                radius: radius,
                theta: theta,
                phi: phi,
                rotationSpeed: 0.005 + Math.random() * 0.01
            };
            
            scene.add(cube);
            cubes.push(cube);
        }
        
        // Add a central glowing sphere
        const sphereGeometry = new THREE.SphereGeometry(1.5, 32, 32);
        const sphereMaterial = new THREE.MeshStandardMaterial({
            color: 0x5DD62C,
            emissive: 0x1A3A0A,
            transparent: true,
            opacity: 0.3
        });
        const sphere = new THREE.Mesh(sphereGeometry, sphereMaterial);
        scene.add(sphere);
        
        // Add particles
        const particlesGeometry = new THREE.BufferGeometry();
        const particlesCount = 500;
        const posArray = new Float32Array(particlesCount * 3);
        
        for (let i = 0; i < particlesCount * 3; i += 3) {
            posArray[i] = (Math.random() - 0.5) * 30;
            posArray[i+1] = (Math.random() - 0.5) * 30;
            posArray[i+2] = (Math.random() - 0.5) * 30;
        }
        
        particlesGeometry.setAttribute('position', new THREE.BufferAttribute(posArray, 3));
        
        const particlesMaterial = new THREE.PointsMaterial({
            size: 0.05,
            color: 0x5DD62C,
            transparent: true,
            opacity: 0.5,
            blending: THREE.AdditiveBlending
        });
        
        const particles = new THREE.Points(particlesGeometry, particlesMaterial);
        scene.add(particles);
        
        // Mouse interaction
        let mouseX = 0;
        let mouseY = 0;
        
        document.addEventListener('mousemove', (event) => {
            mouseX = (event.clientX / window.innerWidth) * 2 - 1;
            mouseY = -(event.clientY / window.innerHeight) * 2 + 1;
        });
        
        // Animation loop
        function animate() {
            requestAnimationFrame(animate);
            
            // Rotate cubes based on mouse
            cubes.forEach(cube => {
                cube.rotation.x += cube.userData.rotationSpeed * 0.5;
                cube.rotation.y += cube.userData.rotationSpeed;
                
                // Orbit around center
                cube.userData.theta += cube.userData.speed;
                
                cube.position.x = cube.userData.radius * Math.sin(cube.userData.phi) * Math.cos(cube.userData.theta + mouseX * 2);
                cube.position.y = cube.userData.radius * Math.sin(cube.userData.phi) * Math.sin(cube.userData.theta + mouseY * 2);
                cube.position.z = cube.userData.radius * Math.cos(cube.userData.phi);
            });
            
            // Rotate sphere slowly
            sphere.rotation.x += 0.003;
            sphere.rotation.y += 0.004;
            
            // Rotate particles
            particles.rotation.y += 0.0007;
            
            renderer.render(scene, camera);
        }
        
        animate();
        
        // Handle resize
        window.addEventListener('resize', () => {
            camera.aspect = window.innerWidth / window.innerHeight;
            camera.updateProjectionMatrix();
            renderer.setSize(window.innerWidth, window.innerHeight);
        });
    }
    
    // ====================================================
    // SECTION 2: 3D GLOBE IN MISSION SECTION
    // ====================================================
    function init3DGlobe() {
        const container = document.getElementById('globe-container');
        if (!container) return;
        
        const scene = new THREE.Scene();
        const camera = new THREE.PerspectiveCamera(45, container.clientWidth / container.clientHeight, 0.1, 1000);
        const renderer = new THREE.WebGLRenderer({ alpha: true, antialias: true });
        
        renderer.setSize(container.clientWidth, container.clientHeight);
        renderer.setPixelRatio(window.devicePixelRatio);
        container.appendChild(renderer.domElement);
        
        camera.position.z = 12;
        
        // Create main globe sphere
        const geometry = new THREE.SphereGeometry(4, 64, 64);
        
        // Load texture
        const textureLoader = new THREE.TextureLoader();
        const globeTexture = textureLoader.load('https://threejs.org/examples/textures/planets/earth_atmos_2048.jpg');
        
        const material = new THREE.MeshPhongMaterial({
            map: globeTexture,
            emissive: 0x112211,
            shininess: 5
        });
        
        const globe = new THREE.Mesh(geometry, material);
        scene.add(globe);
        
        // Add wireframe overlay
        const wireframeGeometry = new THREE.SphereGeometry(4.05, 32, 32);
        const wireframeMaterial = new THREE.MeshBasicMaterial({
            color: 0x5DD62C,
            wireframe: true,
            transparent: true,
            opacity: 0.15
        });
        const wireframeGlobe = new THREE.Mesh(wireframeGeometry, wireframeMaterial);
        scene.add(wireframeGlobe);
        
        // Add atmospheric glow
        const glowGeometry = new THREE.SphereGeometry(4.2, 64, 64);
        const glowMaterial = new THREE.MeshStandardMaterial({
            color: 0x5DD62C,
            emissive: 0x1A3A0A,
            transparent: true,
            opacity: 0.1,
            side: THREE.BackSide
        });
        const glowSphere = new THREE.Mesh(glowGeometry, glowMaterial);
        scene.add(glowSphere);
        
        // Add floating particles around globe
        const particlesGeometry = new THREE.BufferGeometry();
        const particlesCount = 300;
        const posArray = new Float32Array(particlesCount * 3);
        
        for (let i = 0; i < particlesCount; i++) {
            const radius = 5 + Math.random() * 2;
            const theta = Math.random() * Math.PI * 2;
            const phi = Math.acos(2 * Math.random() - 1);
            
            posArray[i*3] = radius * Math.sin(phi) * Math.cos(theta);
            posArray[i*3+1] = radius * Math.sin(phi) * Math.sin(theta);
            posArray[i*3+2] = radius * Math.cos(phi);
        }
        
        particlesGeometry.setAttribute('position', new THREE.BufferAttribute(posArray, 3));
        
        const particlesMaterial = new THREE.PointsMaterial({
            size: 0.1,
            color: 0x5DD62C,
            transparent: true,
            opacity: 0.6,
            blending: THREE.AdditiveBlending
        });
        
        const particles = new THREE.Points(particlesGeometry, particlesMaterial);
        scene.add(particles);
        
        // Add orbital rings
        const ringGeometry = new THREE.TorusGeometry(5.5, 0.05, 16, 100);
        const ringMaterial = new THREE.MeshStandardMaterial({
            color: 0x5DD62C,
            emissive: 0x1A3A0A,
            transparent: true,
            opacity: 0.2
        });
        
        const ring1 = new THREE.Mesh(ringGeometry, ringMaterial);
        ring1.rotation.x = Math.PI / 2;
        scene.add(ring1);
        
        const ring2 = new THREE.Mesh(ringGeometry, ringMaterial);
        ring2.rotation.z = Math.PI / 3;
        ring2.rotation.x = Math.PI / 4;
        scene.add(ring2);
        
        // Add lights
        const light1 = new THREE.PointLight(0x5DD62C, 1, 20);
        light1.position.set(5, 5, 5);
        scene.add(light1);
        
        const light2 = new THREE.PointLight(0x337418, 1, 20);
        light2.position.set(-5, -5, 5);
        scene.add(light2);
        
        const ambientLight = new THREE.AmbientLight(0x404040);
        scene.add(ambientLight);
        
        // Animation
        function animate() {
            requestAnimationFrame(animate);
            
            globe.rotation.y += 0.002;
            wireframeGlobe.rotation.y += 0.002;
            glowSphere.rotation.y += 0.001;
            
            ring1.rotation.y += 0.001;
            ring2.rotation.y += 0.0015;
            ring2.rotation.x += 0.0005;
            
            particles.rotation.y += 0.0005;
            
            renderer.render(scene, camera);
        }
        
        animate();
        
        // Handle resize
        window.addEventListener('resize', () => {
            camera.aspect = container.clientWidth / container.clientHeight;
            camera.updateProjectionMatrix();
            renderer.setSize(container.clientWidth, container.clientHeight);
        });
    }
    
    // ====================================================
    // PARALLAX EFFECT ON SCROLL
    // ====================================================
    function initParallaxEffects() {
        const parallaxImages = document.querySelectorAll('.parallax-image');
        
        window.addEventListener('scroll', () => {
            const scrolled = window.pageYOffset;
            
            parallaxImages.forEach(img => {
                const speed = 0.05;
                const yPos = scrolled * speed;
                img.style.transform = `translateY(${yPos}px) scale(1.05)`;
            });
        });
    }
    
    // ====================================================
    // FLOATING PARTICLES ANIMATION
    // ====================================================
    function initFloatingParticles() {
        const particles = document.querySelectorAll('.particle');
        
        particles.forEach((particle, index) => {
            const speed = 0.5 + Math.random() * 2;
            const direction = Math.random() * Math.PI * 2;
            
            particle.style.animation = `floatParticle ${10 + index * 2}s linear infinite`;
            particle.style.animationDelay = `${index * 0.5}s`;
        });
    }
    
    // Initialize all 3D effects
    initFloatingCubes();
    init3DGlobe();
    initParallaxEffects();
    initFloatingParticles();
    
    // Add tilt effect to cards (using vanilla JS)
    const tiltCards = document.querySelectorAll('[data-tilt]');
    
    tiltCards.forEach(card => {
        card.addEventListener('mousemove', (e) => {
            const rect = card.getBoundingClientRect();
            const x = e.clientX - rect.left;
            const y = e.clientY - rect.top;
            
            const centerX = rect.width / 2;
            const centerY = rect.height / 2;
            
            const deltaX = (x - centerX) / centerX * 10;
            const deltaY = (y - centerY) / centerY * 10;
            
            card.style.transform = `perspective(500px) rotateX(${-deltaY}deg) rotateY(${deltaX}deg) scale(1.02)`;
        });
        
        card.addEventListener('mouseleave', () => {
            card.style.transform = 'perspective(500px) rotateX(0) rotateY(0) scale(1)';
        });
    });
});
